#include "ft_printf.h"

int	len_pointer(size_t p, t_flags *f)
{
	int i;

	i = 0;
	if (p == 0 && f->point == 1)
		return (2);
	else if (p == 0 && f->point == 0)
		return (3);
	while (p)
	{
		p /= 16;
		i++;
	}
	return (i + 2);
}
